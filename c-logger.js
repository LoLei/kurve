class Logger
{
    // +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    // Setup

    constructor(log_level)
    {
        this.log_level_ = log_level;
    }

    // +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    // Methods

    log(level, message)
    {
        if(this.log_level_ >= level)
        {
            console.log(message);
        }
    }
}